public class Cliente {
	
	private	int		cliente_id;
	private	String	cliente_nome;
	private	String	cliente_sobrenome;
	private	String	cliente_doc;
	private	String	cliente_email;
	private	String	cliente_telefone;
	private	String	cliente_celular;
	private	String	cliente_datacad;
	
	public int getCliente_id() {
		return cliente_id;
	}
	public void setCliente_id(int cliente_id) {
		this.cliente_id = cliente_id;
	}
	public String getCliente_nome() {
		return cliente_nome;
	}
	public void setCliente_nome(String cliente_nome) {
		this.cliente_nome = cliente_nome;
	}
	public String getCliente_sobrenome() {
		return cliente_sobrenome;
	}
	public void setCliente_sobrenome(String cliente_sobrenome) {
		this.cliente_sobrenome = cliente_sobrenome;
	}
	public String getCliente_doc() {
		return cliente_doc;
	}
	public void setCliente_doc(String cliente_doc) {
		this.cliente_doc = cliente_doc;
	}
	public String getCliente_email() {
		return cliente_email;
	}
	public void setCliente_email(String cliente_email) {
		this.cliente_email = cliente_email;
	}
	public String getCliente_telefone() {
		return cliente_telefone;
	}
	public void setCliente_telefone(String cliente_telefone) {
		this.cliente_telefone = cliente_telefone;
	}
	public String getCliente_celular() {
		return cliente_celular;
	}
	public void setCliente_celular(String cliente_celular) {
		this.cliente_celular = cliente_celular;
	}
	public String getCliente_datacad() {
		return cliente_datacad;
	}
	public void setCliente_datacad(String cliente_datacad) {
		this.cliente_datacad = cliente_datacad;
	}
	
	
}
